--
--Criaçao de Trigger de Log  : A7PROC.TGAR_ENDE_FILA_MQSE
--Tabela associada           : TB_ENDE_FILA_MQSE
--Data Criaçao               : 29-08-2003 12:55:34
--
CREATE OR REPLACE TRIGGER TGAR_ENDE_FILA_MQSE
	AFTER UPDATE OR DELETE OR INSERT
	ON A7.TB_ENDE_FILA_MQSE
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete, 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A7.TB_LOG_ENDE_FILA_MQSE
			(SG_SIST_DEST,
			CO_EMPR_DEST,
			CO_USUA_ULTI_ATLZ,
			CO_ETCA_TRAB_ULTI_ATLZ,
			DH_ULTI_ATLZ,
			NO_FILA_MQSE,
			IN_TIPO_OPER,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.SG_SIST_DEST,
			:OLD.CO_EMPR_DEST,
			:OLD.CO_USUA_ULTI_ATLZ,
			:OLD.CO_ETCA_TRAB_ULTI_ATLZ,
			:OLD.DH_ULTI_ATLZ,
			:OLD.NO_FILA_MQSE,
			nIN_TIPO_OPER,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_ENDE_FILA_MQSE'
									||'A7.TB_LOG_ENDE_FILA_MQSE'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
	--
END;
