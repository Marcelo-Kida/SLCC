--
--Criaçao de Trigger de Log  : A7PROC.TGAR_OCOR_MESG
--Tabela associada           : TB_OCOR_MESG
--Data Criaçao               : 29-08-2003 12:55:37
--
CREATE OR REPLACE TRIGGER TGAR_OCOR_MESG
	AFTER UPDATE OR DELETE OR INSERT
	ON A7.TB_OCOR_MESG
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete, 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A7.TB_LOG_OCOR_MESG
			(CO_OCOR_MESG,
			IN_ENVI_EMAIL,
			DE_OCOR_MESG,
			DE_ABRV_OCOR_MESG,
			TP_GRAU_SEVE,
			TX_ENDE_EMAIL_NOTI,
			CO_USUA_ULTI_ATLZ,
			CO_ETCA_TRAB_ULTI_ATLZ,
			DH_ULTI_ATLZ,
			IN_TIPO_OPER,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.CO_OCOR_MESG,
			:OLD.IN_ENVI_EMAIL,
			:OLD.DE_OCOR_MESG,
			:OLD.DE_ABRV_OCOR_MESG,
			:OLD.TP_GRAU_SEVE,
			:OLD.TX_ENDE_EMAIL_NOTI,
			:OLD.CO_USUA_ULTI_ATLZ,
			:OLD.CO_ETCA_TRAB_ULTI_ATLZ,
			:OLD.DH_ULTI_ATLZ,
			nIN_TIPO_OPER,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_OCOR_MESG'
									||'A7.TB_LOG_OCOR_MESG'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
	--
END;
