--
--Criaçao de Trigger de Log  : A7PROC.TGAR_ATRB_MESG
--Tabela associada           : TB_ATRB_MESG
--Data Criaçao               : 29-08-2003 12:55:33
--
CREATE OR REPLACE TRIGGER TGAR_ATRB_MESG
	AFTER UPDATE OR DELETE OR INSERT
	ON A7.TB_ATRB_MESG
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete, 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A7.TB_LOG_ATRB_MESG
			(NO_ATRB_MESG,
			DT_FIM_VIGE_ATRB_MESG,
			QT_CASA_DECI_ATRB,
			TP_DADO_ATRB_MESG,
			DE_ATRB_MESG,
			NO_TRAP_ATRB,
			IN_ATRB_PRMT_VALO_NEGT,
			DT_INIC_VIGE_ATRB_MESG,
			QT_CTER_ATRB,
			CO_USUA_ULTI_ATLZ,
			CO_ETCA_TRAB_ULTI_ATLZ,
			DH_ULTI_ATLZ,
			IN_TIPO_OPER,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.NO_ATRB_MESG,
			:OLD.DT_FIM_VIGE_ATRB_MESG,
			:OLD.QT_CASA_DECI_ATRB,
			:OLD.TP_DADO_ATRB_MESG,
			:OLD.DE_ATRB_MESG,
			:OLD.NO_TRAP_ATRB,
			:OLD.IN_ATRB_PRMT_VALO_NEGT,
			:OLD.DT_INIC_VIGE_ATRB_MESG,
			:OLD.QT_CTER_ATRB,
			:OLD.CO_USUA_ULTI_ATLZ,
			:OLD.CO_ETCA_TRAB_ULTI_ATLZ,
			:OLD.DH_ULTI_ATLZ,
			nIN_TIPO_OPER,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_ATRB_MESG'
									||'A7.TB_LOG_ATRB_MESG'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
	--
END;
