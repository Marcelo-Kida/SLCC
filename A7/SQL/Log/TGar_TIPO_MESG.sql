--
--Criaçao de Trigger de Log  : A7PROC.TGAR_TIPO_MESG
--Tabela associada           : TB_TIPO_MESG
--Data Criaçao               : 29-08-2003 12:55:44
--
CREATE OR REPLACE TRIGGER TGAR_TIPO_MESG
	AFTER UPDATE OR DELETE OR INSERT
	ON A7.TB_TIPO_MESG
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete, 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A7.TB_LOG_TIPO_MESG
			(TP_MESG,
			DT_INIC_VIGE_MESG,
			NO_TIPO_MESG,
			TP_NATZ_MESG,
			TP_FORM_MESG_SAID,
			TP_CTER_DELI,
			CO_PRIO_FILA_SAID_MESG,
			DT_FIM_VIGE_MESG,
			CO_USUA_ULTI_ATLZ,
			IN_TIPO_OPER,
			CO_ETCA_TRAB_ULTI_ATLZ,
			DH_ULTI_ATLZ,
			CO_TEXT_XML,
			NO_TITU_MESG,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.TP_MESG,
			:OLD.DT_INIC_VIGE_MESG,
			:OLD.NO_TIPO_MESG,
			:OLD.TP_NATZ_MESG,
			:OLD.TP_FORM_MESG_SAID,
			:OLD.TP_CTER_DELI,
			:OLD.CO_PRIO_FILA_SAID_MESG,
			:OLD.DT_FIM_VIGE_MESG,
			:OLD.CO_USUA_ULTI_ATLZ,
			nIN_TIPO_OPER,
			:OLD.CO_ETCA_TRAB_ULTI_ATLZ,
			:OLD.DH_ULTI_ATLZ,
			:OLD.CO_TEXT_XML,
			:OLD.NO_TITU_MESG,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_TIPO_MESG'
									||'A7.TB_LOG_TIPO_MESG'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
	--
END;
