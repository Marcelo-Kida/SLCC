--
--Criaçao de Trigger de Log  : A7PROC.TGAR_REGR_TRAP_MESG
--Tabela associada           : TB_REGR_TRAP_MESG
--Data Criaçao               : 29-08-2003 12:55:41
--
CREATE OR REPLACE TRIGGER TGAR_REGR_TRAP_MESG
	AFTER UPDATE OR DELETE OR INSERT
	ON A7.TB_REGR_TRAP_MESG
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete, 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A7.TB_LOG_REGR_TRAP_MESG
			(TP_MESG,
			TP_FORM_MESG_SAID,
			SG_SIST_ORIG,
			CO_EMPR_ORIG,
			DT_INIC_VIGE_REGR_TRAP,
			TP_FORM_MESG_ENTR,
			TP_CTER_DELI,
			DT_FIM_VIGE_REGR_TRAP,
			IN_EXIS_REGR_TRNF,
			CO_USUA_ULTI_ATLZ,
			CO_ETCA_TRAB_ULTI_ATLZ,
			DH_ULTI_ATLZ,
			CO_TEXT_XML,
			IN_TIPO_OPER,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.TP_MESG,
			:OLD.TP_FORM_MESG_SAID,
			:OLD.SG_SIST_ORIG,
			:OLD.CO_EMPR_ORIG,
			:OLD.DT_INIC_VIGE_REGR_TRAP,
			:OLD.TP_FORM_MESG_ENTR,
			:OLD.TP_CTER_DELI,
			:OLD.DT_FIM_VIGE_REGR_TRAP,
			:OLD.IN_EXIS_REGR_TRNF,
			:OLD.CO_USUA_ULTI_ATLZ,
			:OLD.CO_ETCA_TRAB_ULTI_ATLZ,
			:OLD.DH_ULTI_ATLZ,
			:OLD.CO_TEXT_XML,
			nIN_TIPO_OPER,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_REGR_TRAP_MESG'
									||'A7.TB_LOG_REGR_TRAP_MESG'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
	--
END;
