--
--Criaçao de Trigger de Log  : A6PROC.TGAR_ITEM_CAIX_TIPO_OPER
--Tabela associada           : TB_ITEM_CAIX_TIPO_OPER
--Data Criaçao               : 14-10-2003 10:54:15
--
CREATE OR REPLACE TRIGGER TGAR_ITEM_CAIX_TIPO_OPER
	AFTER UPDATE OR DELETE OR INSERT
	ON A6.TB_ITEM_CAIX_TIPO_OPER
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete; 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A6.TB_LOG_ITEM_CAIX_TIPO_OPER
			(TP_OPER,
			TP_CNPT,
			IN_ENTR_SAID,
			CO_ITEM_CAIX,
			CO_USUA_ULTI_ATLZ,
			CO_ETCA_TRAB_ULTI_ATLZ,
			DH_ULTI_ATLZ,
			IN_TIPO_OPER,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.TP_OPER,
			:OLD.TP_CNPT,
			:OLD.IN_ENTR_SAID,
			:OLD.CO_ITEM_CAIX,
			:OLD.CO_USUA_ULTI_ATLZ,
			:OLD.CO_ETCA_TRAB_ULTI_ATLZ,
			:OLD.DH_ULTI_ATLZ,
			nIN_TIPO_OPER,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_ITEM_CAIX_TIPO_OPER'
									||'A6.TB_LOG_ITEM_CAIX_TIPO_OPER'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
--
END;
