--
--Criaçao de Trigger de Log  : A6PROC.TGAR_TIPO_OPER_PROD_PJ
--Tabela associada           : TB_TIPO_OPER_PROD_PJ
--Data Criaçao               : 14-10-2003 10:54:16
--
CREATE OR REPLACE TRIGGER TGAR_TIPO_OPER_PROD_PJ
	AFTER UPDATE OR DELETE OR INSERT
	ON A6.TB_TIPO_OPER_PROD_PJ
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete; 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A6.TB_LOG_TIPO_OPER_PROD_PJ
			(CO_PROD,
			TP_OPER,
			DT_INIC_VIGE,
			DT_FIM_VIGE,
			CO_USUA_ULTI_ATLZ,
			CO_ETCA_TRAB_ULTI_ATLZ,
			DH_ULTI_ATLZ,
			IN_TIPO_OPER,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.CO_PROD,
			:OLD.TP_OPER,
			:OLD.DT_INIC_VIGE,
			:OLD.DT_FIM_VIGE,
			:OLD.CO_USUA_ULTI_ATLZ,
			:OLD.CO_ETCA_TRAB_ULTI_ATLZ,
			:OLD.DH_ULTI_ATLZ,
			nIN_TIPO_OPER,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_TIPO_OPER_PROD_PJ'
									||'A6.TB_LOG_TIPO_OPER_PROD_PJ'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
	--
END;
