--
--Criaçao de Trigger de Log  : A8PROC.TGAR_FATO_GERA_ALER_BKOF
--Tabela associada           : TB_FATO_GERA_ALER_BKOF
--Data Criaçao               : 15-10-2003 13:10:44
--
CREATE OR REPLACE TRIGGER TGAR_FATO_GERA_ALER_BKOF
	AFTER UPDATE OR DELETE OR INSERT
	ON A8.TB_FATO_GERA_ALER_BKOF
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete; 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A8.TB_LOG_FATO_GERA_ALER_BKOF
			(CO_FATO_GERA_ALER,
			TP_BKOF,
			IN_ENVI_EMAIL,
			DE_EMAIL_ENVI_ALER,
			TX_ASSU_EMAIL_ENVI_ALER,
			TX_EMAIL_ENVI_ALER,
			IN_EMIS_ALER_TELA,
			IN_ENVI_EMAIL_ANEX,
			CO_USUA_ULTI_ATLZ,
			CO_ETCA_TRAB_ULTI_ATLZ,
			DH_ULTI_ATLZ,
			IN_TIPO_OPER,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.CO_FATO_GERA_ALER,
			:OLD.TP_BKOF,
			:OLD.IN_ENVI_EMAIL,
			:OLD.DE_EMAIL_ENVI_ALER,
			:OLD.TX_ASSU_EMAIL_ENVI_ALER,
			:OLD.TX_EMAIL_ENVI_ALER,
			:OLD.IN_EMIS_ALER_TELA,
			:OLD.IN_ENVI_EMAIL_ANEX,
			:OLD.CO_USUA_ULTI_ATLZ,
			:OLD.CO_ETCA_TRAB_ULTI_ATLZ,
			:OLD.DH_ULTI_ATLZ,
			nIN_TIPO_OPER,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_FATO_GERA_ALER_BKOF'
									|| 'A8.TB_LOG_FATO_GERA_ALER_BKOF'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
--
END;
