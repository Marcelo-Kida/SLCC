--
--Criaçao de Trigger de Log  : A8PROC.TGAR_HORA_LIMI_ITGR_SIST
--Tabela associada           : TB_HORA_LIMI_ITGR_SIST
--Data Criaçao               : 15-10-2003 13:11:00
--
CREATE OR REPLACE TRIGGER TGAR_HORA_LIMI_ITGR_SIST
	AFTER UPDATE OR DELETE OR INSERT
	ON A8.TB_HORA_LIMI_ITGR_SIST
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete; 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A8.TB_LOG_HORA_LIMI_ITGR_SIST
			(SG_SIST,
			CO_EMPR,
			HO_LIMI_ENVI_ITGR,
			CO_USUA_ULTI_ATLZ,
			CO_ETCA_TRAB_ULTI_ATLZ,
			DH_ULTI_ATLZ,
			IN_TIPO_OPER,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.SG_SIST,
			:OLD.CO_EMPR,
			:OLD.HO_LIMI_ENVI_ITGR,
			:OLD.CO_USUA_ULTI_ATLZ,
			:OLD.CO_ETCA_TRAB_ULTI_ATLZ,
			:OLD.DH_ULTI_ATLZ,
			nIN_TIPO_OPER,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_HORA_LIMI_ITGR_SIST'
									|| 'A8.TB_LOG_HORA_LIMI_ITGR_SIST'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
--
END;
