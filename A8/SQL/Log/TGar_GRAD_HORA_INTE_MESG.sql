--
--Criaçao de Trigger de Log  : A8PROC.TGAR_GRAD_HORA_INTE_MESG
--Tabela associada           : TB_GRAD_HORA_INTE_MESG
--Data Criaçao               : 15-10-2003 13:10:50
--
CREATE OR REPLACE TRIGGER TGAR_GRAD_HORA_INTE_MESG
	AFTER UPDATE OR DELETE OR INSERT
	ON A8.TB_GRAD_HORA_INTE_MESG
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete; 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A8.TB_LOG_GRAD_HORA_INTE_MESG
			(CO_GRAD_HORA,
			SQ_ISPB,
			QT_TEMP_MARG_SEGR,
			CO_USUA_ULTI_ATLZ,
			CO_ETCA_TRAB_ULTI_ATLZ,
			DH_ULTI_ATLZ,
			IN_TIPO_OPER,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.CO_GRAD_HORA,
			:OLD.SQ_ISPB,
			:OLD.QT_TEMP_MARG_SEGR,
			:OLD.CO_USUA_ULTI_ATLZ,
			:OLD.CO_ETCA_TRAB_ULTI_ATLZ,
			:OLD.DH_ULTI_ATLZ,
			nIN_TIPO_OPER,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_GRAD_HORA_INTE_MESG'
									|| 'A8.TB_LOG_GRAD_HORA_INTE_MESG'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
--
END;
