--
--Criaçao de Trigger de Log  : A8PROC.TGAR_FATO_GERA_ALER_GRUP_USU
--Tabela associada           : TB_FATO_GERA_ALER_GRUP_USUA
--Data Criaçao               : 15-10-2003 13:10:46
--
CREATE OR REPLACE TRIGGER TGAR_FATO_GERA_ALER_GRUP_USU
	AFTER UPDATE OR DELETE OR INSERT
	ON A8.TB_FATO_GERA_ALER_GRUP_USUA
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete; 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A8.TB_LOG_FATO_GERA_ALER_GRUP
			(CO_FATO_GERA_ALER,
			TP_BKOF,
			CO_GRUP_USUA,
			IN_TIPO_OPER,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.CO_FATO_GERA_ALER,
			:OLD.TP_BKOF,
			:OLD.CO_GRUP_USUA,
			nIN_TIPO_OPER,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_FATO_GERA_ALER_GRUP_USU'
									|| 'A8.TB_LOG_FATO_GERA_ALER_GRUP_USU'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
--
END;
