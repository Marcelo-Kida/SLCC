--
--Criaçao de Trigger de Log  : A8PROC.TGAR_TIPO_JUST_CNCL
--Tabela associada           : TB_TIPO_JUST_CNCL
--Data Criaçao               : 15-10-2003 13:11:07
--
CREATE OR REPLACE TRIGGER TGAR_TIPO_JUST_CNCL
	AFTER UPDATE OR DELETE OR INSERT
	ON A8.TB_TIPO_JUST_CNCL
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete; 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A8.TB_LOG_TIPO_JUST_CNCL
			(TP_JUST_CNCL,
			TP_BKOF,
			NO_TIPO_JUST_CNCL,
			DT_INIC_VIGE,
			DT_FIM_VIGE,
			CO_USUA_ULTI_ATLZ,
			CO_ETCA_TRAB_ULTI_ATLZ,
			DH_ULTI_ATLZ,
			IN_TIPO_OPER,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.TP_JUST_CNCL,
			:OLD.TP_BKOF,
			:OLD.NO_TIPO_JUST_CNCL,
			:OLD.DT_INIC_VIGE,
			:OLD.DT_FIM_VIGE,
			:OLD.CO_USUA_ULTI_ATLZ,
			:OLD.CO_ETCA_TRAB_ULTI_ATLZ,
			:OLD.DH_ULTI_ATLZ,
			nIN_TIPO_OPER,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_TIPO_JUST_CNCL'
									|| 'A8.TB_LOG_TIPO_JUST_CNCL'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
--
END;
