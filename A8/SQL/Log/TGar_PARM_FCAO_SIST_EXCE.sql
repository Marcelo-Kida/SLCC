--
--Criaçao de Trigger de Log  : A8PROC.TGAR_PARM_FCAO_SIST_EXCE
--Tabela associada           : TB_PARM_FCAO_SIST_EXCE
--Data Criaçao               : 15-10-2003 13:11:04
--
CREATE OR REPLACE TRIGGER TGAR_PARM_FCAO_SIST_EXCE
	AFTER UPDATE OR DELETE OR INSERT
	ON A8.TB_PARM_FCAO_SIST_EXCE
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete; 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A8.TB_LOG_PARM_FCAO_SIST_EXCE
			(TP_OPER,
			CO_FCAO_SIST,
			TP_BKOF,
			CO_EMPR,
			SG_SIST,
			CO_LOCA_LIQU,
			CO_GRUP_USUA,
			CO_USUA_ULTI_ATLZ,
			CO_ETCA_TRAB_ULTI_ATLZ,
			DH_ULTI_ATLZ,
			IN_TIPO_OPER,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.TP_OPER,
			:OLD.CO_FCAO_SIST,
			:OLD.TP_BKOF,
			:OLD.CO_EMPR,
			:OLD.SG_SIST,
			:OLD.CO_LOCA_LIQU,
			:OLD.CO_GRUP_USUA,
			:OLD.CO_USUA_ULTI_ATLZ,
			:OLD.CO_ETCA_TRAB_ULTI_ATLZ,
			:OLD.DH_ULTI_ATLZ,
			nIN_TIPO_OPER,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_PARM_FCAO_SIST_EXCE'
									|| 'A8.TB_LOG_PARM_FCAO_SIST_EXCE'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
--
END;
