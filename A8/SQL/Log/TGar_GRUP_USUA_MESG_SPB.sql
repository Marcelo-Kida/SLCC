--
--Criaçao de Trigger de Log  : A8PROC.TGAR_GRUP_USUA_MESG_SPB
--Tabela associada           : TB_GRUP_USUA_MESG_SPB
--Data Criaçao               : 15-10-2003 13:10:57
--
CREATE OR REPLACE TRIGGER TGAR_GRUP_USUA_MESG_SPB
	AFTER UPDATE OR DELETE OR INSERT
	ON A8.TB_GRUP_USUA_MESG_SPB
	FOR EACH ROW
--
DECLARE
	--
	--Variável de controle de Operaçao (1 = Alter ; 2 = Delete; 3 = Insert)
	nIN_TIPO_OPER  NUMBER := 0;
	--
BEGIN
	--
	IF UPDATING THEN
		nIN_TIPO_OPER := 1;
	ELSIF DELETING THEN
		nIN_TIPO_OPER := 2;
	ELSE
		nIN_TIPO_OPER := 3;
	END IF;
	--
	INSERT	INTO A8.TB_LOG_GRUP_USUA_MESG_SPB
			(TP_ACES,
			CO_USUA_ULTI_ATLZ,
			CO_ETCA_TRAB_ULTI_ATLZ,
			DH_ULTI_ATLZ,
			CO_GRUP_USUA,
			CO_MESG_SPB,
			TP_BKOF,
			IN_TIPO_OPER,
			CO_USUA_OPER,
			CO_ETCA_TRAB_OPER,
			DH_OPER)
	VALUES	(:OLD.TP_ACES,
			:OLD.CO_USUA_ULTI_ATLZ,
			:OLD.CO_ETCA_TRAB_ULTI_ATLZ,
			:OLD.DH_ULTI_ATLZ,
			:OLD.CO_GRUP_USUA,
			:OLD.CO_MESG_SPB,
			:OLD.TP_BKOF,
			nIN_TIPO_OPER,
			NULL,
			NULL,
			SYSDATE);
	--
	EXCEPTION
		WHEN OTHERS THEN
			RAISE_APPLICATION_ERROR(-20001,'Erro na execuçao da Trigger TGAR_GRUP_USUA_MESG_SPB'
									|| 'A8.TB_LOG_GRUP_USUA_MESG_SPB'
									|| SQLCODE || ' - '
									|| SUBSTR(SQLERRM, 1, 100));
--
END;
